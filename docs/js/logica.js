// Mostrar menu 2
function mostrarSobreMim(){
    var mostrando = document.getElementById('sobremim');
    if(mostrando.style.display==='inline' ){
        mostrando.style.display='none'
    } else {
        mostrando.style.display='inline'
    }
    
}
